print('Agora vamos fazer o cálculo!\n') # Exibe o objetivo do projeto para o usuário.

# Solicitação dos números para calcular
num1 = int(input('Me informe o primeiro número que deseja calcular: ')) # Solicita que o usuário digite o primeiro número para calcular.
num2 = int(input('Me informe o segundo número que deseja calcular: ')) # Solicita que o usuário digite o segundo número para calcular.

# Escolha da operação
opcao_calculo = input('Qual operação você deseja realizar?\nSoma, Subtração, Multiplicação ou Divisão?\n')
