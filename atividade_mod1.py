    # converter todos os caracteres de uma string para letras minúsculas, sendo bastante útil em situações
    # onde queremos realizar comparações ou manipulações de texto, sem nos preocuparmos
    # com diferenças entre maiúsculas e minúsculas.
    soma = num1 + num2
    # A variável 'soma' armazena o resultado da operação de adição realizada entre
    # os valores das variáveis num1 e num2.
    print('A soma do primeiro número com o segundo número é: ', soma)
    # Exibe para o usuário o resultado da operação de adição.
    break
    # O comando break interrompe o loop while logo assim que a solicitação for atendida.

