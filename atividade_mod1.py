    multiplicacao = num1 * num2
    # A variável 'multiplicacao' armazena o resultado da operação de multiplicação
    # realizada entre os valores das variáveis num1 e num2.
    print('A multiplicação do primeiro número com o segundo número é: ', multiplicacao)
    # Exibe para o usuário o resultado da operação de multiplicação.
    break

