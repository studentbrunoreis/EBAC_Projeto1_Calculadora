    # é acionado o próximo bloco de comando, no caso elif. Se este for válido,
    # o cálculo é executado e o loop é encerrado por meio do comando break.
    subtracao = num1 - num2
    # A variável 'subtracao' armazena o resultado da operação de subtração realizada
    # entre os valores das variáveis num1 e num2.
    print('A subtração do primeiro número com o segundo número é: ', subtracao)
    # Exibe para o usuário o resultado da operação de subtração.
    break

